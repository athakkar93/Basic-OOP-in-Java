package Polymorphism;

public class Math {
	
	public int add(int x, int y) {
		int total = x + y;
		return total;
	}
	
	public int add(int x, int y, int z) {
		int total = x + y + z;
		return total;
	}
	
	public int add(int x, int y, int z, int q) {
		int total = x + y + z + q;
		return total;
	}

}
