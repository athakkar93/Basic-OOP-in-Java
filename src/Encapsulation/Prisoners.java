package Encapsulation;

public class Prisoners {
	
	private int prisonerId;
	private String prisonerName;
	private String prisonerOffense;
	private String lengthOfSentence;
	public int getPrisonerId() {
		return prisonerId;
	}
	public void setPrisonerId(int prisonerId) {
		this.prisonerId = prisonerId;
	}
	public String getPrisonerName() {
		return prisonerName;
	}
	public void setPrisonerName(String prisonerName) {
		this.prisonerName = prisonerName;
	}
	public String getPrisonerOffense() {
		return prisonerOffense;
	}
	public void setPrisonerOffense(String prisonerOffense) {
		this.prisonerOffense = prisonerOffense;
	}
	public String getLengthOfSentence() {
		return lengthOfSentence;
	}
	public void setLengthOfSentence(String lengthOfSentence) {
		this.lengthOfSentence = lengthOfSentence;
	}
	

}
